    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtName.getText();
        String email = txtEmail.getText();
        String pass = String.valueOf(pwfpass.getPassword());
        String cpass = String.valueOf(pwfconfirmpassword.getPassword());
        
        if(verifyFields())
        {
            PreparedStatement pst;
            ResultSet rs;
            String sql = "INSERT INTO `login`( `username`, `email`, `password`) VALUES (?,?,?)";
            
            if(email.trim().equals("email")){
                JOptionPane.showMessageDialog(null, "Enter your Email","Empty Email",2);
            }else if(username.trim().equals("username")){
                JOptionPane.showMessageDialog(null, "Enter your Username","Empty Username",2);
            }else if(pass.trim().equals("password")){
                JOptionPane.showMessageDialog(null, "Enter your Password","Empty Password",2);
            }else if(cpass.trim().equals("password")){
                JOptionPane.showMessageDialog(null, "Enter your Confirm Password","Empty Confirm Password",2);
            }else if(!cbxagree.isSelected()){
                JOptionPane.showMessageDialog(null, "Acceipt Policy","Policy doesn't confirm",2);
            }else{
                String emailPattern = "^[\\w!#$%&’*+/=?`{|}~^-]+(?:\\.[\\w!#$%&’*+/=?`{|}~^-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,6}$";
                Pattern regex = Pattern.compile(emailPattern);
                Matcher matcher = regex.matcher(email);
                if (matcher.find()){
                    try {
                        pst = (PreparedStatement)conn.prepareStatement(sql);
                         pst.setString(1,username);
                         pst.setString(2, email);
                         pst.setString(3, pass);

                         try {
                            if(pst.executeUpdate() != 0){
                                JOptionPane.showMessageDialog(null, "Your Account Has Been Created");
                            }
                            else{
                                JOptionPane.showMessageDialog(null, "Error: Check Your Information");
                            }
                        } catch (Exception e) {                           
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Email khong hop le!!");
                    txtEmail.setText("");
                }                                  
            }
        }                  
    }//GEN-LAST:event_btnLoginActionPerformed

    
    
