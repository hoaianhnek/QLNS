    public boolean verifyFields()
    {
       String yemail = txtEmail.getText();
       String yname = txtName.getText();
       String pass = String.valueOf(pwfpass.getPassword());
       String cpass = String.valueOf(pwfconfirmpassword.getPassword());
       
       //check empty fields
       if(yemail.trim().equals("") || yname.trim().equals("") || pass.trim().equals("") || cpass.trim().equals("")){
           JOptionPane.showMessageDialog(null, "One or more fields are empty","Empty Fields",2);
           return false;
       }
       
       //check if the two password are equals
       else if(!pass.equals(cpass)){
           JOptionPane.showMessageDialog(null, "Password doesn't match","Confirm password",2);
           return false;
       }
       
       //if everything is ok
       else {
           return true;
       }
    }
    
    //create a function to check if the entred username already exists in the database
    public boolean checkUsername(String username){
        PreparedStatement pst;
        ResultSet rs;
        boolean username_exist = false;
        
        Connection conn = DBConnect.connect();
        String sql ="SELECT * FROM `login` WHERE `username` = ? ";
        try {
            pst = (PreparedStatement)conn.prepareStatement(sql);
            pst.setString(1, username);
            rs  = pst.executeQuery();
            
            if(rs.next()){
                username_exist = true;
                JOptionPane.showMessageDialog(null,"This Username is Already Taken, Choose Another One","Username Failed", 2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        return username_exist;
    }
    
    
    
    
